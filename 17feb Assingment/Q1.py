# Q.1.What is MongoDB? Explain non-relational databases in short. In which scenarios it is preferred to use
# MongoDB over SQL databases?

# MongoDB is a popular non-relational, document-oriented database that is designed to store and manage large amounts of unstructured or semi-structured data. In MongoDB, data is stored in flexible JSON-like documents, which makes it easier to handle complex data structures without the need for complex joins or schema changes.

# Non-relational databases, also known as NoSQL databases, are designed to handle large volumes of unstructured or semi-structured data that may not fit well in traditional relational databases. Unlike relational databases, NoSQL databases do not rely on fixed schemas or tables, making it easier to store and manage dynamic, unstructured data.

# MongoDB is preferred over SQL databases in scenarios where:

# High availability and scalability are required: MongoDB's distributed architecture allows for easy scaling and high availability, which is not possible in traditional SQL databases.

# Flexible data structures: MongoDB's document-based approach makes it easy to store and manage complex data structures, making it ideal for scenarios where data structures are constantly changing or evolving.

# Rapid development: MongoDB's flexible schema makes it easier to develop and iterate applications quickly, as there is no need for pre-defined schemas and migrations.

# Large volumes of data: MongoDB is optimized for handling large volumes of data, making it an ideal choice for big data applications.

# Overall, MongoDB is a popular choice for modern, data-intensive applications that require flexibility, scalability, and high availability.