# Q7. Give the order of execution of SQL clauses in an SQL query.
# The order of execution of SQL clauses in an SQL query is as follows:

# FROM: This clause specifies the table or tables from which the data is to be retrieved.

# JOIN: This clause is used to combine data from two or more tables based on a related column between them.

# WHERE: This clause is used to filter the data based on one or more conditions.

# GROUP BY: This clause is used to group the data by one or more columns.

# HAVING: This clause is used to filter the grouped data based on one or more conditions.

# SELECT: This clause is used to select the columns that are to be retrieved.

# DISTINCT: This clause is used to remove duplicate rows from the result set.

# ORDER BY: This clause is used to sort the result set based on one or more columns.

# LIMIT: This clause is used to limit the number of rows that are returned by the query.

# It's important to note that not all SQL queries will use every clause listed above, and the order in which the clauses are used may vary depending on the specific query being executed. However, this is the general order of execution for SQL clauses in a query.