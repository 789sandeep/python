# Q5. Explain ImportError. What is ModuleNotFoundError?
# ImportError is an exception that occurs when a module or package cannot be imported. This can happen for several reasons, such as:

# The module or package does not exist in the current environment.
# The module or package is not installed or is installed in the wrong location.
# There is an error in the module or package that prevents it from being imported.
# Here's an example of an ImportError that occurs when trying to import a non-existent module:

import non_existent_module  # Raises an ImportError
